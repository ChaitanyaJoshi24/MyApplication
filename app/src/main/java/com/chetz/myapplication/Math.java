package com.chetz.myapplication;

public class Math {

    public int addition(int one, int two) {
        int result = one + two;
        return result;
    }

    public int subtraction(int one, int two) {
        int result = one - two;
        return result;
    }

    public int multiplication(int one, int two) {
        int result = one * two;
        return result;
    }

    public int division(int one, int two) {
        int result = one / two;
        return result;
    }
}
